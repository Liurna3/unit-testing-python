class WorkBreakdownStructure():
    def __init__(self):
        self.name = ""
        self.theTasks = ""

    def getName(self):
        return self.name

    def getTheTask(self):
        return self.theTasks

    def setName(self, name):
        self.name = name

    def setTheTask(self, theTasks):
        self.theTasks = theTasks

